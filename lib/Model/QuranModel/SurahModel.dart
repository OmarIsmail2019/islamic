class AyatModel {
  int? code;
  String? status;
  Data? data;

  AyatModel({this.code, this.status, this.data});

  AyatModel.fromJson(Map<String, dynamic> json) {
    code = json['code'];
    status = json['status'];
    data = json['data'] != null ? Data.fromJson(json['data']) : null;
  }
}

